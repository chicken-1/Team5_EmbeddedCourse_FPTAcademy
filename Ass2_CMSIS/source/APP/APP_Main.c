/*******************************************************************************
* Include
*******************************************************************************/
#include "APP_GPIO.h"

/*******************************************************************************
* Definitions
*******************************************************************************/
#define FGPIO_LED	FGPIOB
#define Pin_LED 	4

/*******************************************************************************
* Main function
*******************************************************************************/
int main() {
	HAL_GPIO_Init(FGPIO_LED, Pin_LED);
	while(1) {
		APP_GPIO_Mode1(FGPIO_LED, Pin_LED);
		APP_GPIO_Mode2(FGPIO_LED, Pin_LED);
	}
}

